#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int i;
	int t;

	i = 0;
	t = 1;
	if (s1 && s2)
	{
		while (s1[i] != '\0' || s2[i] != '\0')
		{
			if (s1[i] == s2[i])
				t *= 1;
			else
				t *= 0;
			i++;
		}
		return (t);
	}
	return (0);
}
